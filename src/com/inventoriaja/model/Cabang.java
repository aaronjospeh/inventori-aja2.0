package com.inventoriaja.model;

/**
 *
 * @author Indra Mahesa
 */
public class Cabang {
    private int id;
    private String nama;
    private String alamat;
    private String nohp;
    private String createdAt;

    public Cabang(int id, String nama, String alamat, String nohp, String createdAt) {
        this.id = id;
        this.nama = nama;
        this.alamat = alamat;
        this.nohp = nohp;
        this.createdAt = createdAt;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getNohp() {
        return nohp;
    }

    public void setNohp(String nohp) {
        this.nohp = nohp;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }
    
    
}
